print("=== Программа учета учебного времени ===")

# Запрашиваем количество дней с проверкой
days = 0
while True:
    try:
        days = int(input("Введите количество учебных дней (1-7): "))

        # Проверка, что дни от 1 до 7
        if days < 1:
            print("Ошибка! Дней не может быть меньше 1.")
        elif days > 7:
            print("Ошибка! Дней не может быть больше 7.")
        else:
            break  # Если все правильно, выходим из цикла

    except ValueError:
        print("Ошибка! Нужно ввести число.")

print(f"Хорошо! Вы будете вводить данные для {days} дней.\n")

# Запрашиваем часы для каждого дня
total_hours = 0
day = 1

while day <= days:
    try:
        hours = float(input(f"Сколько часов вы учились в день {day}? "))

        # Проверка, что часы не отрицательные
        if hours < 0:
            print("Ошибка! Часы не могут быть отрицательными.")
        else:
            total_hours += hours
            day += 1  # Переходим к следующему дню

    except ValueError:
        print("Ошибка! Введите число (например: 3.5 или 4).")

# Выводим результат
print("\n" + "=" * 30)
print("ИТОГОВЫЙ ОТЧЕТ:")
print(f"Всего учебных дней: {days}")
print(f"Всего часов учебы: {total_hours:.1f}")
print("=" * 30)